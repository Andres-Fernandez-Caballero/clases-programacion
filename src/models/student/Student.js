class Student {
	/**
	 * @param {string} firstName
	 * @param {string} lastName
	 * @param {string} bornDate
	 * @param {string} dni
	 */
	constructor(firstName, lastName, bornDate, dni) {
		this.firstName = firstName;
		this.lastName = lastName;
		this.bornDate = bornDate;
		this.dni = dni;
	}
}

export default Student;
